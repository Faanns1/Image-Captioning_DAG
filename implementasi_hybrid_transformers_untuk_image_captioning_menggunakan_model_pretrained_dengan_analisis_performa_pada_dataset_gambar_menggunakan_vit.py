# Collate Function & DataLoader
def collate_fn(batch):
    imgs = [item[0].unsqueeze(0) for item in batch]
    imgs = torch.cat(imgs, dim=0)
    caps = [item[1] for item in batch]
    caps = pad_sequence(caps, batch_first=True, padding_value=0)
    return imgs, caps

loader = DataLoader(dataset, batch_size=32, shuffle=True, collate_fn=collate_fn)

# Proses Training
model.train()
print(f"Model siap! Menggunakan device: {device}")
print("Memulai Training...")

for epoch in range(num_epochs):
    total_loss = 0
    for idx, (imgs, caps) in enumerate(loader):
        imgs, caps = imgs.to(device), caps.to(device)

        # Forward prop
        outputs = model(imgs, caps[:, :-1])

        # Loss calculation
        loss = criterion(outputs.reshape(-1, outputs.shape[2]), caps[:, 1:].reshape(-1))

        # Backward prop
        optimizer.zero_grad()
        loss.backward()
        optimizer.step()

        total_loss += loss.item()

        if (idx + 1) % 50 == 0:
            print(f"Epoch [{epoch+1}/{num_epochs}], Step [{idx+1}/{len(loader)}], Loss: {loss.item():.4f}")

    print(f"--- Epoch {epoch+1} Selesai, Average Loss: {total_loss/len(loader):.4f} ---")

import numpy as np

def visualize_prediction(image_index, model, dataset, device):
    model.eval()

    img, actual_cap_indices = dataset[image_index]
    img_input = img.unsqueeze(0).to(device)

    result_caption = ["<SOS>"]
    max_length = 20

    for _ in range(max_length):
        tokens = torch.tensor([dataset.vocab.stoi[w] for w in result_caption]).unsqueeze(0).to(device)
        with torch.no_grad():
            output = model(img_input, tokens)
            best_guess = output.argmax(2)[:, -1].item()
            next_word = dataset.vocab.itos[best_guess]

            if next_word == "<EOS>":
                break
            result_caption.append(next_word)

    img_display = img.permute(1, 2, 0).numpy()
    img_display = img_display * np.array([0.229, 0.224, 0.225]) + np.array([0.485, 0.456, 0.406])
    img_display = np.clip(img_display, 0, 1)

    plt.figure(figsize=(8, 8))
    plt.imshow(img_display)

    actual_caption = [dataset.vocab.itos[idx.item()] for idx in actual_cap_indices if idx.item() not in [0, 1, 2]]

    plt.title(f"AI: {' '.join(result_caption[1:])}\nActual: {' '.join(actual_caption)}", fontsize=12)
    plt.axis("off")
    plt.show()

