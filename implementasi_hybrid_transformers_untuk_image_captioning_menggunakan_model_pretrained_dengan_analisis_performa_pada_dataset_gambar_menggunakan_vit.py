def generate_caption_beam(image_path, model, vocab, transform, device, beam_size=3, max_len=20):
    model.eval()
    img = Image.open(image_path).convert("RGB")
    img_tensor = transform(img).unsqueeze(0).to(device)

    # Memulai dengan token <SOS>
    start_token = vocab.stoi["<SOS>"]
    sequences = [[ [start_token], 0.0 ]] # [ [tokens], score ]

    for _ in range(max_len):
        all_candidates = []
        for seq, score in sequences:
            if seq[-1] == vocab.stoi["<EOS>"]:
                all_candidates.append([seq, score])
                continue

            tokens = torch.tensor(seq).unsqueeze(0).to(device)
            with torch.no_grad():
                output = model(img_tensor, tokens)
                # Ambil probabilitas kata terakhir
                log_probs = torch.log_softmax(output[0, -1, :], dim=0)

            # Ambil n-kandidat terbaik (beam size)
            top_probs, top_indices = log_probs.topk(beam_size)
            for i in range(beam_size):
                candidate = [seq + [top_indices[i].item()], score + top_probs[i].item()]
                all_candidates.append(candidate)

        # Urutkan berdasarkan skor tertinggi dan pilih n-terbaik
        ordered = sorted(all_candidates, key=lambda x: x[1], reverse=True)
        sequences = ordered[:beam_size]

        # Jika semua kandidat sudah <EOS>, berhenti
        if all(s[0][-1] == vocab.stoi["<EOS>"] for s in sequences):
            break

    # Ambil urutan terbaik
    best_seq = sequences[0][0]
    caption = [vocab.itos[idx] for idx in best_seq if idx not in [vocab.stoi["<SOS>"], vocab.stoi["<EOS>"], vocab.stoi["<PAD>"]]]
    return " ".join(caption)

